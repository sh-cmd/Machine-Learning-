# Python code to demonstrate use   
# of math.fsum() function 
  
# fsum() is found in math library 
import math 
  
# range(10) 
print(math.fsum(range(10))) 
  
# Integer list 
arr = [1, 4, 6] 
print(math.fsum(arr)) 
  
# Floating point list 
arr = [2.5, 2.4, 3.09] 
print(math.fsum(arr)) 


